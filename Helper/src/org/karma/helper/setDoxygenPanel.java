    /** Creates new form setDoxygenPanel */
    public setDoxygenPanel(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        Dimension screenDim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation((screenDim.width-this.getWidth())/2,(screenDim.height-this.getHeight())/2);
        
         try
        {
            BufferedReader in = new BufferedReader(new FileReader(RootDir));
          do
            {  
                String str;
                str = in.readLine();
                if(str==null)
                {
                    enableDoxygen.setSelected(false);
                    projectInfo.isDoxygenOn = false;
                    break;
                }
                else 
                {
                    pathToDoxygen.setText(str);
                    enableDoxygen.setSelected(true);
                    projectInfo.isDoxygenOn = true;
                    projectInfo.DoxygenPath = str;
                    break;
                }

            } while(true);
           in.close();
            
            
            
        }
         catch(IOException e)
        {
            System.out.println(""+e);
         enableDoxygen.setSelected(false);
         projectInfo.isDoxygenOn = false;
        }
         
         
        
        
        
        
    }

