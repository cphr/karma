    private void choosePathActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choosePathActionPerformed
        // TODO add your handling code here:
        
        JFileChooser chooser = new JFileChooser();
        //chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("choosertitle");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);

        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
        //System.out.println("getCurrentDirectory(): " + chooser.getCurrentDirectory());
        //System.out.println("getSelectedFile() : " + chooser.getSelectedFile());
        pathToDoxygen.setText(chooser.getSelectedFile()+"");
        enableDoxygen.setSelected(false);
        
        
        } else 
        {
        System.out.println("No Selection ");
        }


    }//GEN-LAST:event_choosePathActionPerformed

    private void enableDoxygenStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_enableDoxygenStateChanged
        // TODO add your handling code here:
       File config = new File(RootDir);
        if (enableDoxygen.isSelected())
        {   String thePathText = pathToDoxygen.getText();
            try
            {
                FileUtils.writeStringToFile(config, thePathText);
                projectInfo.isDoxygenOn = true;
            }
            catch(IOException ex) 
            {System.out.println(ex); }
        }
        else
        {
            try
            {   projectInfo.isDoxygenOn = false;
                config.delete();
            }
            catch(Exception ex) 
            {System.out.println(ex); }
        }
        
        
        
       
    }//GEN-LAST:event_enableDoxygenStateChanged

